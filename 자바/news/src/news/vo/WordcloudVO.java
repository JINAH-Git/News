//기사내용의 명사 자료를 담기위한 VO(Value Object)클래스
package news.vo;

public class WordcloudVO 
{
	String noun; //명사
	String cnt;  //빈도수
	
	public String getNoun() { return noun; }
	public String getCnt()  { return cnt;  }
	
	public void setNoun(String noun) { this.noun = noun; }
	public void setCnt(String cnt)   { this.cnt  = cnt;  }
	
}
